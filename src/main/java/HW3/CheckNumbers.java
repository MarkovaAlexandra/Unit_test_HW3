package HW3;

public class CheckNumbers {
    public boolean evenOddNumber(int n){
        if (n % 2 == 0) {
            return true;
        } else {
            return false;
        }
        }
    public boolean numberInInterval(int n) {
        if (25 < n & n < 100){
            return true;
        }else {
            return false;
        }
        }
    }

